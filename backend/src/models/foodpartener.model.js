import mongoose from "mongoose";

const foodpartenerSchema = new mongoose.Schema(
    {
        name: {
            type: String,
            required: true,
        },
        email: {
            type: String,
            unique: true,
            required: true,
        },
        password: {
            type: String,
            required: true,
        },
    },
    { timestamps: true }
);

export const foodpartenermodel = mongoose.model("foodpartener", foodpartenerSchema);
